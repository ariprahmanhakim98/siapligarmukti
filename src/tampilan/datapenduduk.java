    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
        Object[] Baris = {"NIK","NAMA","JENIS_KELAMIN","TEMPAT_LAHIR","TANGGAL_LAHIR","AGAMA","PENDIDIKAN","PEKERJAAN","STATUS_PERKAWINAN","KEWARGANEGARAAN","RT_RW","ALAMAT"};
        tabmode = new DefaultTableModel(null, Baris);
        tabeldatapenduduk.setModel(tabmode);
        
        String sql = "SELECT * FROM datapenduduk WHERE nama like'%"+txtcari.getText()+"%' ";
        try{
            java.sql.Statement stat=conn.createStatement();
            ResultSet rs=stat.executeQuery(sql);
            while(rs.next()){
                String a = rs.getString("nik");
                String b = rs.getString("nama");
                String c = rs.getString("jenis_kelamin");
                String d = rs.getString("tempat_lahir");
                String e = rs.getString("tanggal_lahir");
                String f = rs.getString("agama");
                String g = rs.getString("pendidikan");
                String h = rs.getString("pekerjaan");
                String i = rs.getString("status_perkawinan");
                String j = rs.getString("kewarganegaraan");
                String k = rs.getString("rt_rw");
                String l = rs.getString("alamat");
                
                String[] data ={a,b,c,d,e,f,g,h,i,j,k,l};
                tabmode.addRow(data);
                kosong();
            }
            
        }catch(Exception e){
        
        }
    }//GEN-LAST:event_btncariActionPerformed

    private void tabeldatapendudukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabeldatapendudukMouseClicked
        int bar = tabeldatapenduduk.getSelectedRow();
        String a = tabmode.getValueAt(bar, 0).toString();
        String b = tabmode.getValueAt(bar, 1).toString();
        String c = tabmode.getValueAt(bar, 2).toString();
        String d = tabmode.getValueAt(bar, 3).toString();
        String e = tabmode.getValueAt(bar, 4).toString();
        String f = tabmode.getValueAt(bar, 5).toString();
        String g = tabmode.getValueAt(bar, 6).toString();
        String h = tabmode.getValueAt(bar, 7).toString();
        String i = tabmode.getValueAt(bar, 8).toString();
        String j = tabmode.getValueAt(bar, 9).toString();
        String k = tabmode.getValueAt(bar, 10).toString();
        String l = tabmode.getValueAt(bar, 11).toString();
       
        
        txtnik.setText(a);
        txtnama.setText(b);
        if(c.equals("laki-laki")){
            jeniskelamin1.setSelected(true); 
            jeniskelamin2.setSelected(false);
        }
        else {
            jeniskelamin1.setSelected(false); 
            jeniskelamin2.setSelected(true);
        }
        txttempatlahir.setText(d);
        txttgllahir.setText(e);
        txtagama.setText(f);
        txtpendidikan.setText(g);
        txtpekerjaan.setText(h);
        txtstatusperkawinan.setText(i);
        txtkewarganegaraan.setText(j);
        txtrtrw.setText(k);
        txtalamat.setText(l);
    }//GEN-LAST:event_tabeldatapendudukMouseClicked

