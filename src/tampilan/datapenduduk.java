    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        String sql="INSERT INTO datapenduduk values(?,?,?,?,?,?,?,?,?,?,?,?)";//////////
        try{
            PreparedStatement stat=conn.prepareStatement(sql);
            stat.setString(1, txtnik.getText());
            stat.setString(2, txtnama.getText());
            String jeniskelamin="";
            if(jeniskelamin1.isSelected()) jeniskelamin="laki-laki";
            else jeniskelamin="perempuan";
            stat.setString(3, jeniskelamin);
            stat.setString(4, txttempatlahir.getText());
            stat.setString(5, txttgllahir.getText());
            stat.setString(6, txtagama.getText());
            stat.setString(7, txtpendidikan.getText());
            stat.setString(8, txtpekerjaan.getText());
            stat.setString(9, txtstatusperkawinan.getText());
            stat.setString(10, txtkewarganegaraan.getText());
            stat.setString(11, txtrtrw.getText());
            stat.setString(12, txtalamat.getText());
        
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil Di Simpan" );
            kosong();
            txtnik.requestFocus();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Data Gagal Di Simpan" );
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

