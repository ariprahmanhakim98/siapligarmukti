    protected void datatable(){
      Object[] Baris = {"NIK","NAMA","JENIS_KELAMIN","TEMPAT_LAHIR","TANGGAL_LAHIR","AGAMA","PENDIDIKAN","PEKERJAAN","STATUS_PERKAWINAN","KEWARGANEGARAAN","RT_RW","ALAMAT"};
        tabmode = new DefaultTableModel(null, Baris);
        tabeldatapenduduk.setModel(tabmode);
        
        String sql = "select * from datapenduduk";
        
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("nik");
                String b = hasil.getString("nama");
                String c = hasil.getString("jenis_kelamin");
                String d = hasil.getString("tempat_lahir");
                String e = hasil.getString("tanggal_lahir");
                String f = hasil.getString("agama");
                String g = hasil.getString("pendidikan");
                String h = hasil.getString("pekerjaan");
                String i = hasil.getString("status_perkawinan");
                String j = hasil.getString("kewarganegaraan");
                String k = hasil.getString("rt_rw");
                String l = hasil.getString("alamat");
                
                String[] data={a,b,c,d,e,f,g,h,i,j,k,l};
                tabmode.addRow(data);
            }
            
        }catch(Exception e){
            
        }
    }

