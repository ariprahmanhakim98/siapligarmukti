    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        try{
            String sql="UPDATE datapenduduk set nama=?, tempat_lahir=?, tanggal_lahir=?, jenis_kelamin=?, kewarganegaraan=?, pekerjaan=?, agama=?, alamat=? WHERE nik=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtnama.getText());
            stat.setString(2, txttempatlahir.getText());
            stat.setString(3, txttgllahir.getText());
            String jk="";
            if(jk1.isSelected()) jk="laki-laki";
            else jk="perempuan";
            stat.setString(4, jk);
            stat.setString(5, txtkewarganegaraan.getText());
            stat.setString(6, txtpekerjaan.getText()); 
            stat.setString(7, txtagama.getText());
            stat.setString(8, txtalamat.getText());
            stat.setString(9, txtnik.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil Di Ubah" );
            kosong();
            txtnik.requestFocus();
            datatable();
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Data Gagal Di Ubah" );
        }
    }//GEN-LAST:event_btnubahActionPerformed

    private void tabeldatapendudukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabeldatapendudukMouseClicked
        int bar = tabeldatapenduduk.getSelectedRow();
        String a = tabmode.getValueAt(bar, 0).toString();
        String b = tabmode.getValueAt(bar, 1).toString();
        String c = tabmode.getValueAt(bar, 2).toString();
        String d = tabmode.getValueAt(bar, 3).toString();
        String e = tabmode.getValueAt(bar, 4).toString();
        String f = tabmode.getValueAt(bar, 5).toString();
        String g = tabmode.getValueAt(bar, 6).toString();
        String h = tabmode.getValueAt(bar, 7).toString();
        String i = tabmode.getValueAt(bar, 8).toString();
        
        txtnik.setText(a);
        txtnama.setText(b);
        txttempatlahir.setText(c);
        txttgllahir.setText(d);
        if(e.equals("laki-laki")){
            jk1.setSelected(true); 
            jk2.setSelected(false);
        }
        else {
            jk1.setSelected(false); 
            jk2.setSelected(true);
        }
        txtkewarganegaraan.setText(f);
        txtpekerjaan.setText(g);
        txtagama.setText(h);
        txtalamat.setText(i);
    }//GEN-LAST:event_tabeldatapendudukMouseClicked

