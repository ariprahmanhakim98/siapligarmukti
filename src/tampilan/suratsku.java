    protected void datatable(){
        Object[] Baris = {"NIK","NAMA","TEMPAT_LAHIR","TANGGAL_LAHIR","JENIS_KELMIN","KEWARGANEGARAAN","PEKERJAAN","AGAMA","ALAMAT"};
        tabmode = new DefaultTableModel(null, Baris);
        tabeldatapenduduk.setModel(tabmode);
        
        String sql = "select * from datapenduduk";
        
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("nik");
                String b = hasil.getString("nama");
                String c = hasil.getString("tempat_lahir");
                String d = hasil.getString("tanggal_lahir");
                String e = hasil.getString("jenis_kelamin");
                String f = hasil.getString("kewarganegaraan");
                String g = hasil.getString("pekerjaan");
                String h = hasil.getString("agama");
                String i = hasil.getString("alamat");
                
                String[] data={a,b,c,d,e,f,g,h,i};
                tabmode.addRow(data);
            }
            
        }catch(Exception e){
            
        }
    }
